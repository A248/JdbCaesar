package space.arim.jdbcaesar.it;

import space.arim.jdbcaesar.transact.IsolationLevel;

public enum Vendor {

	HSQLDB,
	
	MARIADB,
	
	POSTGRES,
	
	SQLITE,
	
	H2;
	
	private Vendor() {

	}
	
	/**
	 * Gets how this vendor specifies auto incrementing primary keys, also known as identity
	 * or serial keys.
	 * 
	 * @return how this vendor specifies auto increment keys
	 */
	public String getIdentitySpecification() {
		switch (this) {
		case HSQLDB:
			return "INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY";
		case MARIADB:
			return "INT AUTO_INCREMENT PRIMARY KEY";
		case POSTGRES:
			return "SERIAL PRIMARY KEY";
		case SQLITE:
			return "INTEGER PRIMARY KEY AUTOINCREMENT";
		case H2:
			return "INT IDENTITY NOT NULL PRIMARY KEY";
		default:
			throw new IllegalStateException("I do not know myself");
		}
	}
	
	IsolationLevel defaultIsolation() {
		switch (this) {
		case SQLITE:
			return IsolationLevel.SERIALIZABLE;
		default:
			return IsolationLevel.REPEATABLE_READ;
		}
	}
	
}
